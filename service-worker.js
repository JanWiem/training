/* Satz – Service Worker
   Cache-first für die App-Shell, damit die App offline (z. B. im Keller-Gym) startet.
   Bei jedem Deploy CACHE hochzählen, dann wird neu gecacht. */
const CACHE = "satz-v3";
const SHELL = [
  "./",
  "./index.html",
  "./manifest.json",
  "./icons/icon-192.png",
  "./icons/icon-512.png",
  "./icons/icon-maskable-512.png"
];

self.addEventListener("install", (e) => {
  e.waitUntil(caches.open(CACHE).then((c) => c.addAll(SHELL)).then(() => self.skipWaiting()));
});

self.addEventListener("activate", (e) => {
  e.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(keys.filter((k) => k !== CACHE).map((k) => caches.delete(k)))
    ).then(() => self.clients.claim())
  );
});

self.addEventListener("fetch", (e) => {
  const req = e.request;
  if (req.method !== "GET") return;
  // App-Shell & Icons: cache-first. Fonts/alles andere: network, dann Cache als Fallback.
  e.respondWith(
    caches.match(req).then((cached) => {
      if (cached) return cached;
      return fetch(req)
        .then((res) => {
          // Google-Fonts & SheetJS (cdnjs) zur Laufzeit mitcachen
          if (req.url.includes("fonts.g") || req.url.includes("cdnjs")) {
            const copy = res.clone();
            caches.open(CACHE).then((c) => c.put(req, copy));
          }
          return res;
        })
        .catch(() => cached);
    })
  );
});
